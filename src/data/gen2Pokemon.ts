export type Gen2Pokemon = {
  id: number;
  name: string;
  types: string[];
  image: string;
};

export const gen2Pokemon: Gen2Pokemon[] = Array.from(
  { length: 100 },
  (_, i) => {
    const id = i + 152;
    return {
      id,
      name: NAMES[i],
      types: TYPES[id] ?? ["normal"],
      image: `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/${id}.png`,
    };
  },
);
