export type Gen1Pokemon = {
  id: number;
  name: string;
  types: string[];
  image: string;
};

// Generate all 151 with a loop — no repetition
export const gen1Pokemon: Gen1Pokemon[] = Array.from(
  { length: 151 },
  (_, i) => {
    const id = i + 1;
    return {
      id,
      name: NAMES[id],
      types: TYPES[id] ?? ["normal"],
      image: `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/${id}.png`,
    };
  },
);
